function sexto(){
    let dia=parseInt(prompt('Ingrese un numero para saber a que mes corresponde'))

    if (dia<=0){
        console.log('Numero no valido')
    }
    else if (dia<=31){
        console.log('Corresponde a Enero')
    }
    else if(dia<=59){
        console.log('Corresponde a Febrero')
    }
    else if (dia<=90){
        console.log('Corresponde a Marzo')
    }
    else if(dia<=120){
        console.log('Corresponde a Abril')
    }
    else if (dia<=151){
        console.log('Corresponde a Mayo')
    }
    else if (dia<=181){
        console.log('Corresponde a Junio')
    }
    else if (dia<=212){
        console.log('Corresponde a Julio')
    }
    else if (dia<=243){
        console.log('Corresponde a Agosto')
    }
    else if (dia<=273){
        console.log('Corresponde a Septiembre')
    }
    else if (dia<=304){
        console.log('Corresponde a Octubre')
    }
    else if (dia<=334){
        console.log('Corresponde a Noviembre')
    }
    else if (dia<=365){
        console.log('Correponde a Diciembre')
    }

}

